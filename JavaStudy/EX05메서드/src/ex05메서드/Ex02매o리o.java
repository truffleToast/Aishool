package ex05메서드;

public class Ex02매o리o {

	public static int minus(int num1, int num2) {
		return num1 - num2;
		// 매개변수가 있고 리턴타입이 있다.
		// 전달인자가 있고 리턴타입자리에 데이터 타입이 있다.
		// 2개의 숫자를 빼기 해주 결과를 받을수 있는 minus라는 매서드
	}

	public static void main(String[] args) {
		
	System.out.println(minus(8,3));

	}

}
